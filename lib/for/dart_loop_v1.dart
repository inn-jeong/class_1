void main(){
  //반복문
  //반복적인 패턴이 보인다면 for, for in, while, do while 구문을 사용하자.
  //반복문의 보통 조건문과 continue, break 와 함게 많이 사용된다.

  print("홍길동");
  print("홍길동");
  print("홍길동");
  //위 코드초럼 반복적인 패턴이 보인다면 반복을 사용하자.
  for(int i=0; i<10; i++){
    //조건식이 참 일때 수행되는 영역
    print("이순신");
  }
  for(int i=0; i>=1; i++){
    print(i);
  }
}